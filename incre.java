public class incre {
    public static void main(String[] args) {
        int x=5,y;
        y=++x;
        System.out.println(y);
        int a=6,b;
        b=a++;
        System.out.println(b +" " + a);
        int r=5,s;
        s=--r;
        System.out.println(s);
        int i=4,j;
        j=i--;
        System.out.println(i+" "+j);
    }
}
